        if(!jTextFieldSearch.getText().equals(""))
        {
            Customer c= new Customer();
            Vehicle x = new Vehicle();
            
            Customer returned = c.SearchCustomer(Integer.parseInt(jTextFieldSearch.getText()));
            
            if(returned.getID() > 0)
            { 
                jTextFieldID.setText("" + returned.getID());
                jTextFieldCustomer.setText("" + returned.getName());
                jTextFieldNumber.setText("" + returned.getRentedCar().getNumber());
                
                 
        Date d = null;
        try {
            d=new SimpleDateFormat("dd-MM-yyyy").parse(returned.getBookDate());
        } catch (ParseException ex) {
            Logger.getLogger(UpdateandDeleteEmployee.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Wrong date");
        }
       jDateChooserDate.setDate(d);
                
               jLabelAnswer.setText("Found ...");

                
            }
            else 
                jLabelAnswer.setText("ID not Found......");  
            
        }
        else 
            jLabelAnswer.setText("Fill The Search Field");
       
