    private void jButtonReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReturnActionPerformed
        // TODO add your handling code here:
        
       if(!jTextFieldNumber.getText().equals("")) 
       {
        Customer c = new Customer();
        Vehicle v = new Vehicle();
        Driver d = new Driver();
        Customer Returned = c.SearchCustomer(Integer.parseInt(jTextFieldSearch.getText()));
        Vehicle returned = v.SearchVehicle(Returned.getRentedCar().getNumber());
       
        Driver D  = d.DriverCar(returned.getNumber());
        
        if(D.getCar().getNumber() > 0)
        {
            D.setFree(0);
            Vehicle temp = new Vehicle();
            D.setCar(temp);
            Customer f = new Customer();
            D.setClient(f);
        }
       
       
        if(Returned.getID()>0)
        {    
        returned.setStatus("Unrented");
        returned.setUseAs("");
        
        Vehicle tmp=new Vehicle();
        Returned.setRentedCar(tmp);
        
        if(D.UpdateDriver() && Returned.UpdateCustomer()&&returned.UpdateVehicle()){
        resetPanel();
        jLabelAnswer.setText("Vehicle Returned Successfully");
        }
       }
        else
            jLabelAnswer.setText("ID not Found");
       }
       else
           jLabelAnswer.setText("Enter a valid Customer ID.....");
       
    }//GEN-LAST:event_jButtonReturnActionPerformed
    
    
