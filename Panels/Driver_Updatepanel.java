     protected void setpanelData(Driver x)
{
jTextFieldname.setText(""+x.getName());
jTextFieldAdress.setText(""+x.getAddress());
jTextFieldphone.setText(""+x.getPhoneNumber());
jTextFieldsalary.setText(""+x.getSalary());
jTextFieldPhotoPath.setText(""+ x.getPhoto());
try{
                ImageIcon icon = new ImageIcon(ImageIO.read(new File(x.getPhoto())));
                jLabelPhoto.setIcon(icon);
            }
            catch(IOException e){
                System.out.println(e);
            }

Date d;
    try {
        d=new SimpleDateFormat("dd-MM-yyyy").parse(x.getDateOfBirth());
        jDateChooserBirthDate.setDate(d);
    } 
    catch (ParseException e) {
        System.out.println(e);
    }

if(x.getGender().equals("Male"))
{
    jComboBoxGender.setSelectedIndex(0);
}
else 
    jComboBoxGender.setSelectedIndex(1);

if(x.getExprience().equals("Expert"))
{
  jComboBox2.setSelectedIndex(0);
}
else 
   jComboBox2.setSelectedIndex(1);


}
