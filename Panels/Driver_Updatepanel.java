    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        if (!jTextFieldPhotoPath.getText().equals("")  &&!jTextFieldsalary.getText().equals("")&&!jTextFieldname.getText().equals("")&&!jTextFieldAdress.getText().equals("")&&!jTextFieldphone.getText().equals(""))
        {
        
        Driver x=new Driver ();
        String s;
        
        x.setID(Integer.parseInt(jTextFieldsearch.getText()));
        
        
        s=exc.ISnumeric(jTextFieldsalary.getText(),"Salary");   
        x.setSalary(Double.parseDouble(s));
        
        
        x.setAddress((jTextFieldAdress.getText()));
        x.setName(jTextFieldname.getText());
        
        if(jComboBoxGender.getSelectedIndex() == 0)
        {
            x.setGender("Male");
        }
        else 
            x.setGender("Female");
        
        
        if(jComboBox2.getSelectedIndex() == 0)
        {
            x.setExprience("Expert");
        }
        else 
            x.setExprience("Beginner");
        
        SimpleDateFormat  dateFormat=new SimpleDateFormat("dd-MM-yyyy");
        s=dateFormat.format(jDateChooserBirthDate.getDate()); 
        x.SetDateOfBirth(s);
        
        
        s=exc.ISnumeric(jTextFieldphone.getText(),"Phone Number");
        x.setPhoneNumber(exc.checkPositive(s));
        
        x.setPhoto(jTextFieldPhotoPath.getText());
        
        if (x.UpdateDriver())
        {
        jLabelanswer.setText("Updated successfully...!");
        resetpanelData();    
        }
        else 
        {
         jLabelanswer.setText("Failed to update...!");
        }            
        }
                else { jLabelanswer.setText("Missing information please complete it...!");}

        
        
    }//GEN-LAST:event_jButtonUpdateActionPerformed

