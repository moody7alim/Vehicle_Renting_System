    private void resetPanelData() {
       jTextFieldAddress.setText("");
       jTextFieldNameEmployee.setText("");
       jTextFieldPassword.setText("");
       jTextFieldPhoneNumber.setText(""); 
       jComboBoxQualification.setSelectedIndex(-1);
       jTextFieldSearchKey.setText("");
       jTextFieldUserName.setText("");
       
       jDateChooserDateofJoin.setCalendar(null);
       jDateChooserDateofBirth.setCalendar(null); 
    }

    private void setPanelData(Employee returned) {
       
        
        jTextFieldAddress.setText(""+returned.getAddress());
        
        
        Date d = null;
        try {
            d=new SimpleDateFormat("dd-MM-yyyy").parse(returned.getDateOfJoin());
        } catch (ParseException ex) {
            Logger.getLogger(UpdateandDeleteEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
       jDateChooserDateofJoin.setDate(d);
       
       
       try {
            d=new SimpleDateFormat("dd-MM-yyyy").parse(returned.getDateOfBirth());
        } catch (ParseException ex) {
            Logger.getLogger(UpdateandDeleteEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
       jDateChooserDateofBirth.setDate(d); 
       
       
       jTextFieldNameEmployee.setText(""+returned.getName());
       jTextFieldPassword.setText(""+returned.getPassword());
       jTextFieldPhoneNumber.setText(""+returned.getPhoneNumber());
       jTextFieldUserName.setText(""+returned.getUserName());
       if(returned.getQualification().equals("Graduate"))
           jComboBoxQualification.setSelectedIndex(0);
       else if(returned.getQualification().equals("Expert"))
           jComboBoxQualification.setSelectedIndex(1);
       else
           jComboBoxQualification.setSelectedIndex(2);
    
    if(returned.getGender().equals("Female"))
    {
        jComboBoxGender.setSelectedIndex(0);
    }
    else
        jComboBoxGender.setSelectedIndex(1);
    
    
    }
        
