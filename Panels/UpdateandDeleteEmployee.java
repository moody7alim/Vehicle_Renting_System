    private void jButtonsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsearchActionPerformed
        // TODO add your handling code here:
        if(!jTextFieldSearchKey.getText().equals("")) {
            Employee x = new Employee();
            Employee returned = x.SearchEmployee(jTextFieldSearchKey.getText());
            if (!returned.getUserName().isEmpty()) {
                setPanelData(returned);
            }

            else {
                jLabelSucessorfail.setText("Not Found ...!");
            }
        }
        else {
            jLabelSucessorfail.setText("Missing required Fields ... !  Please, Search By Name before submit ...!");
        }

    }//GEN-LAST:event_jButtonsearchActionPerformed

