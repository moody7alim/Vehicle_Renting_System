    private void jButtonAddDriverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddDriverActionPerformed
        // TODO add your handling code here:
        
        if (!jTextFieldphoto.getText().equals("")&&!jTextFieldsalary.getText().equals("")&&!jTextFieldName.getText().equals("")&&!jTextFieldAddress.getText().equals("")&&!jTextFieldphone.getText().equals(""))
        {
        
        Driver x=new Driver ();
        String s;
        
        s=exc.ISnumeric(jTextFieldID.getText()," Driver ID");
        x.setID(Integer.parseInt(exc.checkPositive(s)));
        
        s=exc.ISnumeric(jTextFieldsalary.getText()," Salary");   
        x.setSalary(Double.parseDouble(exc.checkPositive(s)));
        
        x.setAddress((jTextFieldAddress.getText()));
        
        x.setName(jTextFieldName.getText());
      
        
        if(jComboBoxExp.getSelectedIndex() == 0)
        {
            x.setExprience("Expert");
        }
        else 
            x.setExprience("Beginner");
        
        boolean b=true;
        SimpleDateFormat dateFormat=new SimpleDateFormat("dd-MM-yyyy");
        try{
                s=dateFormat.format(jDateChooserBirthDate.getDate());
                x.SetDateOfBirth(s); 
            }
            catch(Exception ex){
                Logger.getLogger(Driver_Addpanel.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"No date entered");
                b=false;
                        
            }
       
        s=exc.ISnumeric(jTextFieldphone.getText(),"Phone Number");
        x.setPhoneNumber(exc.checkPositive(s));
       
        
        if(jComboBoxGender.getSelectedIndex() == 0)
        {
            x.setGender("Male");
        }
        else 
            x.setGender("Female");
        
        
        
        x.setPhoto(jTextFieldphoto.getText());
        if ( b==true && x.AddDriver() )
        {
        jLabel8.setText("Added Successfully.....");
        resetpanelData();
        }else {
            jLabel8.setText("Failed to add this driver.....");
        }
                     }
        else {
                jLabel8.setText("missing information please complete it.....");}
 
        
        
        
        
        
        
    }//GEN-LAST:event_jButtonAddDriverActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String filename = "image1.png";
        JFileChooser chooser = new JFileChooser(); 
          chooser.showOpenDialog(null);
          File f = chooser.getSelectedFile();
          if(f.isFile()) 
            filename = f.getAbsolutePath();
          
            jTextFieldphoto.setText(filename);
            try{
                ImageIcon icon = new ImageIcon(ImageIO.read(new File(filename)));
                imagebox.setIcon(icon);
            }
            catch(IOException e){
                System.out.println(e);
            }
        
            
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
